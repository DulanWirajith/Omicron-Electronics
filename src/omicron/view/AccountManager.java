    private void transcation() {
        try {
            String transcationFrom = (String) jComboBox1.getSelectedItem();
            String transcationTo = (String) jComboBox2.getSelectedItem();
            double amount = Double.parseDouble(jTextField1.getText());
            double transcationFromAmount = Double.parseDouble(jLabel4.getText());
            if (transcationFromAmount >= amount) {
                boolean transactionConfirm = AccountController.transcationConfirming(transcationFrom, transcationTo, amount);
                if (transactionConfirm) {
                    JOptionPane.showMessageDialog(this, "Transcation Successfull...");
                }

            } else {
                JOptionPane.showMessageDialog(this, "You haven't Enough Money to Transcation...");
            }

        } catch (ClassNotFoundException ex) {
            Logger.getLogger(AccountManager.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "Error in your Inserted Data.. Please Check Again..");
            Logger.getLogger(AccountManager.class.getName()).log(Level.SEVERE, null, ex);
        }

    }

    private void loadAccounts() {
        try {
            ArrayList<Account> allAccounts = AccountController.loadAllAccounts();
            for (Account account : allAccounts) {
                jComboBox1.addItem(account.getAccountName());
                jComboBox2.addItem(account.getAccountName());
            }
            jComboBox1.setSelectedItem("Income Account");
            jComboBox2.setSelectedItem("Bank Account");
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(OrderManagement.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(OrderManagement.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
